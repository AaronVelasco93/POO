public class CondicinalMultiple {
    
    public static void main(String[] args) {
        
        int a =15 ;

        switch (a){
            case 1:
                System.out.println("Buen dia");
                break;
            case 2:
                System.out.println("Buena tarde");
                break;
            case 15:
                 System.out.println("Buena noche");
                break;
            default:
                System.out.println("Sin conincidencia");
                break;
        }
        
        
    }
    
}
